// 采用局部平移法
void SelectSort1(int array[], int n) {
	int i=0,j=1,k=0;
	int temp;
	for(j=1; j<n; j++) {
		temp = array[j];
		for(i=0; i<j; i++) {
			if(array[i] > array[j]) {
				// 局部平移
				for(k=j; k>i; k--) {
					array[k] = array[k-1];
				}
				array[i] = temp;
				break;
			}
		}
	}
}

// 采用局部冒泡法代替局部平移法
void SelectSort2(int array[], int n) {
	int i=0,j=1;
	int temp;
	for(j=1; j<n; j++) {
		for(i=j; i>0; i--) {
			if(array[i]<array[i-1]) {
				temp = array[i];
				array[i] = array[i-1];
				array[i-1] = temp;
			}
		}
	}
}

